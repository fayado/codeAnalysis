package com.mimo.analysis.core.model;

/**
 * 类来源枚举，标识一个类是通过哪种分析方式被发现的。
 *
 * 在分析流程中，同一个类可能同时被 Java Agent（运行时采集）和
 * 静态字节码分析（ASM）两种方式检测到，也可能只被其中一种发现。
 * 该枚举用于标记类的发现来源，便于后续合并和统计。
 */
public enum ClassOrigin {

    /**
     * 仅通过 Java Agent 运行时采集发现。
     * 表示该类在应用运行过程中被实际加载和使用。
     */
    AGENT,

    /**
     * 仅通过静态字节码分析（ASM）发现。
     * 表示该类在字节码中存在引用，但运行时未必被实际加载。
     */
    STATIC,

    /**
     * 同时被 Agent 运行时采集和静态字节码分析发现。
     * 表示该类既在字节码中被引用，也在运行时被实际加载使用。
     */
    BOTH
}
